func (mgr *ProjectManager) ExporterDB(Project Project) {
	sqlgenerator := Sqlgenerator{}
	db := Project.GetBDD()
	models := db.GetModels()
	projectName := Project.GetNom()
	sqlgenerator.PostgresSQLExporter(models, projectName)
	sqlgenerator.PostgresCRUDExporter(models, projectName)
	fmt.Printf("Exportation des modèles terminée pour le projet : %s\n", projectName)
}



func (mgr *ProjectManager) ExporterAPI(Project Project) {
	apiMaker := GoApiMaker{}
	projectName := Project.GetNom()
	bdd := Project.GetBDD()
	apiMaker.setupFileArch(projectName)
	models := bdd.GetModels()
	api := Project.GetRestApi()
	apiMaker.modelAPIExporter(models, projectName)
	apiMaker.controllerAPIExporter(api.GetEndpoints(), projectName)
	apiMaker.routesAPIExporter(api.GetEndpoints(), projectName)
	apiMaker.configAPIExporter(projectName)
	apiMaker.middlewareAPIExporter(projectName)
	apiMaker.mainAPIExporter(api.GetEndpoints(), projectName)
	apiMaker.writeModSumFile(projectName)
	fmt.Printf("Exportation de l'API terminée pour le projet : %s\n", projectName)
}

func (mgr *ProjectManager) setupStaticArch(name string) {
	projectPath := fmt.Sprintf("%s/%s/static/", config.PROJECT_DIR, name)
	dirList := []string{
		"css",
		"js",
		"assets",
	}
	for _, val := range dirList {
		config.CheckCreateDir(projectPath + val)
	}
}

func (mgr *ProjectManager) renderBlocksToHTML(blocks []any) string {
	var sb strings.Builder
	for _, b := range blocks {
		block, ok := b.(map[string]any)
		if !ok {
			continue
		}

		tag := fmt.Sprintf("%v", block["tag"])
		content := fmt.Sprintf("%v", block["content"])
		className := fmt.Sprintf("%v", block["className"])
		id := fmt.Sprintf("%v", block["id"])

		// Gestion des balises auto-fermantes
		if tag == "img" {
			sb.WriteString(fmt.Sprintf("<img src=\"%s\" class=\"%s\" data-id=\"%s\" />", content, className, id))
			continue
		}

		sb.WriteString(fmt.Sprintf("<%s class=\"%s\" data-id=\"%s\">", tag, className, id))
		sb.WriteString(content)

		if children, exists := block["children"].([]any); exists && len(children) > 0 {
			sb.WriteString(mgr.renderBlocksToHTML(children))
		}

		sb.WriteString(fmt.Sprintf("</%s>", tag))
	}
	return sb.String()
}

func (mgr *ProjectManager) ExporterStaticSite(Project Project) {
	projectName := Project.GetNom()
	site := Project.GetSiteStatique()
	mgr.setupStaticArch(projectName)

	// 1. Génération du CSS global
	cssPath := fmt.Sprintf("%s/static/css/style.css",  projectName)
	cssFile, _ := os.Create(cssPath)
	cssFile.WriteString("/* Generated by TurboStack */\nbody { margin: 0; font-family: sans-serif; }\n")

	// On pourrait ici itérer pour extraire tous les styles JSON des blocs
	cssFile.Close()

	// 2. Génération des pages HTML
	for _, page := range site.GetPages() {
		pageName := strings.ToLower(strings.ReplaceAll(page.GetNom(), " ", "_"))
		filePath := fmt.Sprintf("%s/static/%s.html", projectName, pageName)

		file, err := os.Create(filePath)
		if err != nil {
			continue
		}

		var sb strings.Builder
		sb.WriteString("<!DOCTYPE html>\n<html lang=\"en\">\n<head>\n")
		sb.WriteString("\t<meta charset=\"UTF-8\">\n")
		fmt.Fprintf(&sb, "\t<title>%s</title>\n", page.GetNom())
		sb.WriteString("\t<link rel=\"stylesheet\" href=\"css/style.css\">\n")
		sb.WriteString("</head>\n<body>\n")

		// Conversion du contenu JSON en HTML
		content := page.GetContent()
		anyBlocks := make([]any, len(content))
		for i, v := range content {
			anyBlocks[i] = v
		}
		sb.WriteString(mgr.renderBlocksToHTML(anyBlocks))

		sb.WriteString("\n</body>\n</html>")

		file.WriteString(sb.String())
		file.Close()
	}

	fmt.Printf("Exportation du site statique terminée : %s\n", projectName)
}

