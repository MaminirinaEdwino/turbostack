// mapToGoType convertit les types UI en types Go
func (mgr *ProjectManager) mapToGoType(uiType string) string {
	switch strings.ToLower(uiType) {
	case "int":
		return "int"
	case "string", "text":
		return "string"
	default:
		return "string"
	}
}

