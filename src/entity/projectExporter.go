// generateGormTags transforme les contraintes de l'UI en chaîne de tags GORM
func (mgr *ProjectManager) generateGormTags(field Champs) string {
	var tags []string

	for _, c := range field.GetConstraint() {
		cStr := strings.ToLower(fmt.Sprintf("%v", c))

		switch cStr {
		case "primary key":
			tags = append(tags, "primaryKey")
		case "unique":
			tags = append(tags, "unique")
		case "not null":
			tags = append(tags, "not null")
		case "autoincrement":
			tags = append(tags, "autoIncrement")
		}

		if strings.HasPrefix(cStr, "relation:") {
			// Pour les relations, on peut indexer le champ automatiquement
			tags = append(tags, "index")
		}
	}

	if field.GetDefaultValue() != nil && field.GetDefaultValue() != "" {
		tags = append(tags, fmt.Sprintf("default:%v", field.GetDefaultValue()))
	}

	if len(tags) > 0 {
		return fmt.Sprintf("`gorm:\"%s\"`", strings.Join(tags, ";"))
	}
	return ""
}

// generatePgTags transforme les contraintes en chaîne de tags pour la lib go-pg
func (mgr *ProjectManager) generatePgTags(field Champs) string {
	var tags []string
	for _, c := range field.GetConstraint() {
		cStr := strings.ToLower(fmt.Sprintf("%v", c))
		switch cStr {
		case "primary key":
			tags = append(tags, "pk")
		case "unique":
			tags = append(tags, "unique")
		case "not null":
			tags = append(tags, "notnull")
		}
	}

	if field.GetDefaultValue() != nil && field.GetDefaultValue() != "" {
		tags = append(tags, fmt.Sprintf("default:%v", field.GetDefaultValue()))
	}

	if len(tags) > 0 {
		return fmt.Sprintf("`pg:\",%s\"`", strings.Join(tags, ","))
	}
	return ""
}
